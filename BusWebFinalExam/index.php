 <li><p>1. List and discuss five areas of the EULA that stood out to you while reading.</p>
     <p>2. Did the EULA create any conflicts with how you currently use the software? If so, in what ways? How would you propose to resolve the conflict?</p><p>3. Most users click the “I Agree” button without reading the EULA, why is this not a best practice?</p> <p>4. How could EULA's be redesigned in order to better inform users of the software?</p></li>
