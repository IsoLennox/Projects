                        <div class="myAnswer">
                            
                          <p>I chose to read the End User License Agreement for <a href="http://winscp.net/eng/index.php">WinSCP</a>, an open source free SFTP client, FTP client, WebDAV client and SCP client for Windows. I chose this software specifically because it is the one I abuse the most, yet know nothing about its terms. Many of the tools I could have written about are evolving, competing softwares that I may jump between so often that I would create an obsolscent case study out of in this particular case. WinSCP is one of the few tools that I have stuck with and prefer to stay loyal to.</p>  
                            
                          <p>Here's what I learned:</p>  
                         
                            <img src="img/winSCP-agreement.PNG" title="WinSCP user agreement upon install" />
                            
                            <p>When installing software, such as WinSCP, you are greeted by the EULA that's we've all grown to ignore. I think most of us know that there are things in there that we would rather be blissful and ignorant to rather than take responsibility for handling software like literate adults, and understanding the full intentions and restrictions that apply. Take a look at WinSCP's EULA:</p>
                        
                         
                            <div id="scrollboxSm">
              <?php require_once('inc/EULA.inc'); ?>
  </div><!-- end scrollbox-->
                        
                       <p>The license actualy is a bit humorous, stating, "The licenses for most software and other practical works are designed to take away your freedom to share and change the works. By contrast, the GNU General Public License is intended to guarantee your freedom to share and change all versions of a program.."</p>
<p>I like that they point out that the term "free" as in "free software", as the case is here, means freedom-- not price. The freedom provided is the given right to distribute copies of the software, even if you charge. This freedom also permits that you have access to the source sode and can change the program or use it to create distributive works. However, you cannot change the license itself. The "catch" is, by accepting the terms and distributing copies of the software you  accept the responsibility to respect the freedom of others.</p>
<p>This means, if you distribute the software, whether for free or for a charge, this unchanged license remains the same, and they can then do what they wish to the software just as you were given the right to. You must make sure they have access to the source code and this license, so they know their rights.</p>
<p>Luckily, this EULA did not give me any conflicts with how I use this software. However, I did learn what is covered, and expected to be covered (read clause 11: Patents) in a license. By reading this EULA, I understand more risks and vulnerabilities that could apply to me in the furutre. I will most likely refer to this nd other EULA's in the future when authoring my own.</p>
<p>Most users click the “I Agree” button without reading the EULA. However, is this not the best practice. The EULA clearly stated, in this case, that by accepting the terms there are responsibilities ad liabilities being placed onto the aceptee. There are also terms that are delightful to learn such as the allowances and rights you are given when accepting terms. In both cases, it is important to know exactly what the software you are using expects of you. Clearly, if you intend on making profit or not from any software or product, there is possibility to run into unexpected issues which could later turn into your resposibility, just as you had accepted.</p>
 <p>EULA's in general could benefit from a redesign in order to better inform users of the software, especially by containing less jargon and conveluted definitions. In the Open Source License Agreement I chose to define, the authorship was very unclear, and it took me multiple times to re-read the same pagges and cross examining definitions to understand what was being said.</p>
<p>This particular EULA was very clear, well-worded, and even humorous. The voice of the EULA was friendly and far from condescending. There was no jargon; each word was fully digested and appreciated by this individual.  </p> 
