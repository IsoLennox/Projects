			<h3>Freelancing Rates</h3>
			<ul>
	<ol><div class="query">
 <li><p>What factors should be considered when setting an hourly rate for freelancing? Prior to learning about these factors, how would you have set an hourly rate for freelancing?</p></li>
        
        
         </div><!-- END QUERY -->
                        
                        <div class="myAnswer">
        
        
        <p>There are plenty of 'hourly-rate' calculators online that will offer a simple one-size-fits most solution to how to determine your hourly rate as a freelancer. Many may argue that those tools may simply be for statistical use rather than literal-- Or simply a tool for clients to assume their developers are ripping them off. The question still poses, how do you set an hourly rate on your own?</p>
                            
                          <p>  Immediately when thinking about this question, the word that comes to mind is, "relative."</p>
            
          <p>  I know, it's a sophisticated word that really shows how educated and palate-enriched I've become over the past few years. However, each developer and client will have a mix of their own needs that will always be unique. In my opinion, even a developer should never have a fixed hourly rate. It should fluctuate depending on the factors I've become familiar with:</p>
           <p> <strong>Overhead charges</strong> such as rent, supplies, software, insurance, taxes, and a base salary expectancy comparative to what someone in your field is expected to make. If you bid too low, you will undercut yourself and the market you serve. If you list your price too high, you'll have problems finding work. Charges will also fluctuate depending on if you pay for extra plugins or software for a single clients needs.</p>
           <p> <strong>Experience</strong>: are you charging for senior experience, or do you need their project as a skill-building piece? Sometimes you may have to undercut yourself in order to gain a reputation that may benifit you in the long run, such as being able to show future clients that you built something for so-and-so. How impressive!</p>
           <p> <strong>Services</strong>: are you offering maintenance after the site is published? Are you offering technical support or teaching them how to use what you've been leased to create for them?</p>
           <p><p> <strong>Location</strong>: Where are you working? Are you paying for office space, or are you working from home? How much overhead is reasonable to charge based on your work environment?</p>
           <p> <strong>How big is the project?</strong> When creating a contract with a client, most clients do not want to see the price go up as more and more time goes by. Plan your project well, and create a fixed rate based on how much time you think you will spend on it, as well as calculating overhead and software charges. That way, the client will be much more understanding when prices go up after something outside of the contract gets added later, due to their request rather than you taking longer than expected.</p>
          
                            </p>
        
        <p>A really fun cost calculator from <a href="http://thenuschool.com/">thenuschool.com</a> based it's result on the factors:</p>
         
        <p>What is your hourly rate? (My answer: $20)</p>
        <p>How many hours to you estimate working? (200)</p>
        <p>Is te project interesting? (meh)</p>
        <p>After the project... (will look good on my portfolio)</p>
        <p>Based on these answers, I should charge $7,200 with a lowest price ("break-even") price of $4,400.</p>
        <p>Can it be that easy? In this case, the factors are a bit unclear and vague, leaving little room to adjust calculating factors bassed on location or otherwise. As of December 2014, this website offers an online class "<a href="http://thenuschool.com/the-pricing-class/">How To Price Like A Pro</a>", which brings up the idea that it may be useful to invest time into getting to understand your financial needs and how to, indubitably, <em>price like a pro</em>.</p>
        
        <p> It's okay to experiement with your rates. You don't have to come up with the perfect plan in the beginning. Ater playing with your rates and charges for a few projects over the span of a year or two, you will have a better understanding of your needs, as well as what a client will expect and agree to. Then, you may be able to create a template for how you charge clients.</p>
                            <p> Before I considered some of these factors, I would have most likely created a simple algorithm to determine an hourly rate based on a salary and would have undercut myself. I feel that having a spreadsheet as part of your contract to account for and calculate fees and charges is a fantastic idea, as it is flexible and allows you to draw a very simple map for your client, which makes both parties happy.</p>
        
        
        
        
