        <li>
			<h3>Developer Liabilities</h3>
			<ul>
				    <ol>
 
        <div class="query">
  <li><p>In what ways is a developer liable for unknown security issues?</p></li>
   </div><!-- END QUERY -->
                        
                        <div class="myAnswer">
                        
                        
 

<p>Liability laws and costs provide a way to avoid clients being delivered badly designed, poorly implemented features, inadequate testing and security vulnerabilities from software bugs by raising the risk of raising the developers' costs rather than the clients. This creates a gap between con artists-- creating bad software with intention of the customer coming back to fix it-- and developers who work hard to create a standard of best interest from the beginning, regardless if it is in their financial best interests.</p>
<p>Liability forces software companies to think twice before changing something, and to protect the data they're entrusted with. Though it may be easier (and more desirable) to blame a malicious attacker, Liability means that those in the best position to fix the problem are actually responsible for the problem. </p>
<p>This is good news until a developer is faced with an issue that was not expected or designed to avoid. How do you test against security threats you don’t know exist in your mobile app? Security attacks can be caused by a third-party component, as well as in your own software. The difference bewteen Known vulnerabilities, and Unknown is simply that Known vulnerabilities have already been found and reported, while Unknown vulnerabilities are vulnerabilities that have not yet been found. </p>
<p>It is in a developers best interest to keep looking for security vulnerabilitiyes by getting familiar with all used third-party software packages and libraries as well as their licenses. Licenses can bind the developer to additional liabilities.  Open source and third party components are constantly evolving, so it is a good idea to do routine follow ups on updates to stay up to date with this information. Think about it,  if you have software that becomes out of date or the security gets breached often-- no one blames the attacker, they blame you for having poor software. The users perception is what counts, and it's up to you as the developer to ensure that as vulnerabilities evolve, so does your software.</p>

